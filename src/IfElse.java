public class IfElse {
    public static void main(String[] args) {
        String day = "Monday";
        if (day=="Monday"){
            System.out.println("Today is Monday");
        }
        else if(day=="tuesday"){
            System.out.println("Today is Tueday");
        } else if (day=="Wednesday"){
            System.out.println("Today is Wednesday");
        }
    }
}
