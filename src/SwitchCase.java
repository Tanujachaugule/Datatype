public class SwitchCase {
    public static void main(String[] args) {
        String day = "Monday";
        switch(day){
            case "Monday":
                System.out.println("Today is Monday");
            case "Tuesday":
                System.out.println("Today is Tuesday");
            case "Wednesday":
                System.out.println("Today is Wednesday");
            case "Sunday":
                System.out.println("Today is Sunday");
        }
    }
}
